					<p>Kharif crops, also known as monsoon crops, are grown during the rainy season in India, which typically begins in June and lasts until September. These crops thrive under warm temperatures, high humidity, and consistent rainfall. Key examples include rice, maize, bajra (millet), jowar, ragi, tur (pigeon pea), moong (green gram), groundnut, soybean, cotton, and others.</p>
